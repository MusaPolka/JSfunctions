function max(arg1, arg2){
    var max = Math.max(arg1, arg2)
    return max;
}

function min(arg1, arg2){
    var min = Math.min(arg1, arg2)
    return min;
}

function qwad(a, n){
    var result = Math.pow(a, n)
    return result;
}

console.log(max(5, 10));

console.log(min(5, 30));

console.log(qwad(5, 3))